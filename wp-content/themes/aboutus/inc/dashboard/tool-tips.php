		$(function() {
		
		var eventsFired = ($.cookie('eventsFired') != null)
			    ? $.cookie('eventsFired')
			    : 0;
					console.log(eventsFired);

		if (eventsFired == 0) {
			$('#tool-tips').show();
			$(".tool-tips-modal").each(function(){
				$(this).find(".next-step").click(function(){
					var nextStep = $(this).attr("rel");
					$(this).parent().parent().parent().removeClass("in").hide();
					$("#" + nextStep).show(function(){
						$(this).addClass("in");
					});
			        var target_offset = $("#" + nextStep).offset();
			        var target_top = target_offset.top - 200;
			        $('html, body').animate({scrollTop:target_top}, 300);
				});
				$(this).find(".close, .close-step").click(function(){
					$("#tool-tips").remove();
					eventsFired++; // <-- now equals 1, won't fire again until reload
					$.cookie('eventsFired', eventsFired);
				});
				
			});
		}
		});
